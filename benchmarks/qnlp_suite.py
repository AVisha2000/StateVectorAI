#!/usr/bin/env python3
"""Comprehensive QNLP benchmark suite: every hot-swappable quantum site.

Variants (all share one pipeline; the swap is pure config):
  classical    : transformer baseline
  gru-64       : classical recurrent reference
  q-embed      : words-as-quantum-states token embedding (DisCoCat-inspired)
  q-ffn        : VQC feed-forward
  q-attn-proj  : attention output projection -> VQC
  q-attn-qkv   : Q,K,V generated by one VQC
  q-block      : quantum attention + quantum FFN
  q-full       : quantum embedding + attention + FFN (maximal insertion)

Every run is recorded in the SQLite results DB keyed by
(suite, variant, dataset, seed, steps); re-invocations SKIP finished runs,
so the sweep is resumable across calls/machines (run it as-is on a GPU
box later and only missing cells execute). Plot with plot_suite.py.

Usage:
    python benchmarks/qnlp_suite.py --dataset text --seeds 0 1 --steps 300
    python benchmarks/qnlp_suite.py --variants q-full --force
"""
from __future__ import annotations

import argparse
import dataclasses
import statistics
import sys
import time
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

from qllm.config import (  # noqa: E402
    DataConfig,
    ExperimentConfig,
    ModelConfig,
    QuantumConfig,
    TrackingConfig,
    TrainConfig,
    to_flat_dict,
)
from qllm.resultsdb import ResultsDB  # noqa: E402
from qllm.train.loop import fit  # noqa: E402

Q = QuantumConfig(n_qubits=4, n_circuit_layers=2, ansatz="reuploading", readout="zz")
_BASE = dict(
    d_model=64, n_heads=4, n_blocks=2, d_ff=256, max_seq_len=128, quantum=Q
)

VARIANTS: dict[str, ModelConfig] = {
    "classical": ModelConfig(**_BASE),
    "gru-64": ModelConfig(arch="gru", rnn_hidden=64),
    "q-embed": ModelConfig(**_BASE, embed_type="quantum"),
    "q-ffn": ModelConfig(**_BASE, ffn_type="quantum"),
    "q-attn-proj": ModelConfig(**_BASE, attn_type="quantum_proj"),
    "q-attn-qkv": ModelConfig(**_BASE, attn_type="quantum_qkv"),
    "q-block": ModelConfig(**_BASE, attn_type="quantum_proj", ffn_type="quantum"),
    "q-full": ModelConfig(
        **_BASE,
        embed_type="quantum",
        attn_type="quantum_proj",
        ffn_type="quantum",
    ),
}


def data_cfg(dataset: str) -> DataConfig:
    if dataset == "text":
        return DataConfig()
    if dataset in ("ising", "markov"):
        return DataConfig(
            kind="monitored_ising" if dataset == "ising" else "markov_control",
            gen_qubits=6,
            gen_measured=1,
            gen_sequences=64,
            gen_len=2048,
            gen_theta_x=0.6,
            gen_steps_per_token=2,
            gen_seed=0,
            markov_order=3,
        )
    raise ValueError(f"Unknown dataset '{dataset}'")


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--suite-name", default="qnlp-v1")
    parser.add_argument("--dataset", choices=("text", "ising", "markov"),
                        default="text")
    parser.add_argument("--variants", nargs="+", choices=list(VARIANTS),
                        default=list(VARIANTS))
    parser.add_argument("--seeds", type=int, nargs="+", default=[0, 1])
    parser.add_argument("--steps", type=int, default=300)
    parser.add_argument("--db", default="results/qllm_results.db")
    parser.add_argument("--force", action="store_true",
                        help="re-run even if a result row exists")
    args = parser.parse_args()

    db = ResultsDB(args.db)
    t_all = time.time()
    ran = skipped = 0

    for name in args.variants:
        for seed in args.seeds:
            if not args.force and db.exists(
                args.suite_name, name, args.dataset, seed, args.steps
            ):
                skipped += 1
                print(f"skip {name:12s} seed={seed} (already in DB)")
                continue

            cfg = ExperimentConfig(
                model=VARIANTS[name],
                train=TrainConfig(
                    seed=seed,
                    steps=args.steps,
                    batch_size=16,
                    seq_len=64,
                    lr=1e-3,
                    weight_decay=0.01,
                    grad_clip=1.0,
                    eval_every=100,
                    eval_batches=8,
                ),
                data=data_cfg(args.dataset),
                tracking=TrackingConfig(
                    experiment="qllm-qnlp",
                    run_name=f"{args.suite_name}-{args.dataset}-{name}-s{seed}",
                    log_quantum_diagnostics=False,
                ),
            )
            res = fit(cfg, verbose=False, out_dir="results")
            s = res["summary"]
            db.record(
                suite=args.suite_name,
                variant=name,
                dataset=args.dataset,
                seed=seed,
                steps=args.steps,
                n_params=s["n_params"],
                val_loss=s["val_loss"],
                val_ppl=s["val_ppl"],
                val_bpc=s["val_bpc"],
                wall_seconds=s["wall_seconds"],
                resources=s.get("resources"),
                config=to_flat_dict(cfg),
                manifest=res["manifest"],
            )
            ran += 1
            print(
                f"done {name:12s} seed={seed}  params={s['n_params']:7,d}  "
                f"val_ppl={s['val_ppl']:.3f}  ({s['wall_seconds']:.0f}s)"
            )

    rows = db.fetch(args.suite_name, args.dataset)
    if rows:
        print(f"\n=== {args.suite_name} / {args.dataset} ===")
        print(f"{'variant':12s} {'params':>8s} {'val ppl (mean±std)':>20s} {'n':>3s}")
        by = {}
        for r in rows:
            by.setdefault(r["variant"], []).append(r)
        for name, rs in sorted(by.items(), key=lambda kv: statistics.mean(
            x["val_ppl"] for x in kv[1]
        )):
            ppls = [x["val_ppl"] for x in rs]
            std = statistics.stdev(ppls) if len(ppls) > 1 else 0.0
            print(
                f"{name:12s} {rs[0]['n_params']:8,d} "
                f"{statistics.mean(ppls):12.3f} ± {std:.3f} {len(ppls):3d}"
            )
    print(f"\nran {ran}, skipped {skipped}, wall {time.time() - t_all:.0f}s "
          f"-> {args.db}")


if __name__ == "__main__":
    main()
